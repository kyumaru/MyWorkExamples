            precioCosto.setText(A.ejecutarRetornando("select preciocosto from producto where idproducto ='" + s +"'"));
            porcentajeUtilidad.setText(A.ejecutarRetornando("select porcentajeutilidad from producto where idproducto ='" + s +"'"));
