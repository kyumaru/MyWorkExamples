    private void botonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAceptarActionPerformed
        // TODO add your handling code here:
        if(this.botonAceptar.getText().equals("Actualizar")){
          if(!(nombre.getText().equals("") && precioCosto.getText().equals("") 
           && idProveedor.getText().equals("") && porcentajeUtilidad.getText().equals("") &&cantidadMinima.getText().equals("") &&
            cantidadActual.getText().equals(""))){
           if((Integer.parseInt(cantidadMinima.getText()) < Integer.parseInt(cantidadMaxima.getText()))&& (Integer.parseInt(cantidadMaxima.getText())>= Integer.parseInt(cantidadActual.getText()))){
        
            try {
                A.ejecutarSql("select actualizarproducto('" + ss + "','"+ nombre.getText()+"','" +idProveedor.getText()+"','"+precioCosto.getText()
                        +"','"+porcentajeUtilidad.getText()+"','"+cantidadActual.getText()+"','"+cantidadMinima.getText()+"','"+cantidadMaxima.getText()+"','" + Boolean.toString(esExento.isSelected())+"')");
            } catch (SQLException ex) {
                Logger.getLogger(InformacionProducto.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(null, "Producto actualizado");
        }
          }
        }
            
        this.setVisible(false);
    }//GEN-LAST:event_botonAceptarActionPerformed

