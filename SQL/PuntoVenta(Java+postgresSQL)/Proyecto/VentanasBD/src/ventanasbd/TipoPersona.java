    private void jButtonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAceptarActionPerformed
        // TODO add your handling code here:
        String cedula = this.jTextFieldCedula.getText();
    if(!cedula.equals("")){
        if( this.personaFisica.isSelected() ){
            
            if( tipoPersona == 1 ){
            
                new Persona(cedula).setVisible(true);
                
            }
            else  if( tipoPersona == 2 ) {
            
               new EliminarPersonaFisica().setVisible(true);
                
            }
                    
            else  if( tipoPersona == 3 ){
        
                //OJO: aqui debe llenar toda la informacion de usuario ya en la base de datos 
                // y modificar lo que sea necesario
                
                new ModificarPersonaFisica(cedula,1).setVisible(true);
                
                //new Persona().setVisible(true);
            }
            else  if( tipoPersona == 4 ){
        
                //OJO: aqui debe llenar toda la informacion de usuario ya en la base de datos 
                // y modificar lo que sea necesario
                
                new ModificarPersonaFisica(cedula,2).setVisible(true);
                
                //new Persona().setVisible(true);
            }

            this.setVisible(false);
        }

        else if(this.jRadioButtonJuridica.isSelected()){
    
            if( tipoPersona == 1 ){
                
                new PersonaJuridica(this.jTextFieldCedula.getText()).setVisible(true);
            }
            else  if( tipoPersona == 2 ) {
            
               new EliminarPersonaJuridica().setVisible(true);
                
            }
                    
            else  if( tipoPersona == 3 ){
                
                new ModificarPersonaJuridica().setVisible(true);
            }

            this.setVisible(false);
            

        }
        else{
        
            JOptionPane.showMessageDialog(null, "Debe escoger al menos un tipo de persona");
        }
    }else{
        JOptionPane.showMessageDialog(null, "Debe ingresar una cedula");
    }
        
    }//GEN-LAST:event_jButtonAceptarActionPerformed

