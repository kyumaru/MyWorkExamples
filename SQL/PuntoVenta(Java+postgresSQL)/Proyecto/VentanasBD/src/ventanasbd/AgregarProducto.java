    private void botonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAceptarActionPerformed
        // TODO add your handling code here:
        if(!(idproducto.getText().equals("") && nombre.getText().equals("") && precioCosto.getText().equals("") 
           && idProveedor.getText().equals("") && porcentajeUtilidad.getText().equals("") &&cantidadMinima.getText().equals("") &&
            cantidadActual.getText().equals(""))){
        if((Integer.parseInt(cantidadMinima.getText()) < Integer.parseInt(cantidadMaxima.getText()))&& (Integer.parseInt(cantidadMaxima.getText())>= Integer.parseInt(cantidadActual.getText()))){
        try {
            A = new db_conect();
            A.conexion();
                A.ejecutarSql("select insertarproductonuevo('" + idproducto.getText() + "','"+ nombre.getText()+"','" +precioCosto.getText()+"','"+ idProveedor.getText()
                        +"','"+Boolean.toString(esExento.isSelected())+"','"+porcentajeUtilidad.getText()+"','827','"+
                        cantidadMinima.getText()+"','"+cantidadActual.getText()+"','"+cantidadMaxima.getText()+"','" + tipoVenta.getText()+ "')");
            }catch (SQLException ex) {
            System.err.println( ex.getMessage() );
        }
            JOptionPane.showMessageDialog(null, "Producto agregado");
        this.setVisible(false);
        }
        else{
            JOptionPane.showMessageDialog(null, "La cantidad mínima debe ser menor que la cantidad máxima");
        }
        
        }
        else {
            JOptionPane.showMessageDialog(null, "DEBE LLENAR TODOS LOS CAMPOS");
        }
    }//GEN-LAST:event_botonAceptarActionPerformed

