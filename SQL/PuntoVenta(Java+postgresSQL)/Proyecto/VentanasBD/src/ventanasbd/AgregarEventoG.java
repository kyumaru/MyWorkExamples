    private void agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarActionPerformed


        EventosGoogle evento = new EventosGoogle();

        int logger = evento.logger( campoCorreo.getText(), new String( campoPass.getPassword() ) );

        if( logger == 0 && jDateChooser.getDate() != null && (this.jCampoTitulo.getText() != "" && this.jCampoTexto.getText() != "")){
        
            evento.agregarEvento( this.jCampoTitulo.getText(), this.jCampoTexto.getText(),horaInicial,horaFinal );
            
            campoCorreo.setText(null);
            
            campoPass.setText(null);
   
        }
        else{
        
            JOptionPane.showMessageDialog(null, "faltan campos o la contrasenia y pass son incorrectos");
        }
        

    }//GEN-LAST:event_agregarActionPerformed

