    private void Actualizar_TablaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Actualizar_TablaActionPerformed
        // TODO add your handling code here:
      int selectedRow = -1;
      
         selectedRow = this.jTable1.getSelectedRow();
         DBTableModel model = (DBTableModel) this.jTable1.getModel();
      if(!(selectedRow == -1)){
         new Actualizar_Sucursal(model.getValueAt(selectedRow, 0).toString()).setVisible(true);
      }
      else{
          JOptionPane.showMessageDialog(null, "Debe seleccionar una fila de la tabla");
      }
    }//GEN-LAST:event_Actualizar_TablaActionPerformed

