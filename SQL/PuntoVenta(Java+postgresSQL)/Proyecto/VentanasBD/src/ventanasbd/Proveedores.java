    private void eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarActionPerformed
        // TODO add your handling code here:
        String ced = "";
        int resp=JOptionPane.showConfirmDialog(null,"¿Está seguro de eliminar un  Proveedor?");
        if (JOptionPane.OK_OPTION == resp){
            try {
                //JOptionPane.showMessageDialog(null,"Selecciona opción Afirmativa");
                ced = JOptionPane.showInputDialog("Digite la cedula del proveedor a eliminar");
                if(!ced.equals("")){
                A.ejecutarSql("update proveedor set \"estaActivo\" = 'false' where cedula ='"+ ced +"'");
                JOptionPane.showMessageDialog(null, "PROVEEDOR ELIMINADO");
                }
            } catch (SQLException ex) {
                Logger.getLogger(ModificarPersonaFisica.class.getName()).log(Level.SEVERE, null, ex);
            }
            this.setVisible(false);
        }
        else{
            //JOptionPane.showMessageDialog(null,"No selecciona una opción afirmativa");
        }
    }//GEN-LAST:event_eliminarActionPerformed

